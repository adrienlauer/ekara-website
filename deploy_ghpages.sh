#!/bin/sh

if [ -z $1 ]; then
    BRANCH="master"
    REPO="ekara-platform.github.io"
    DIR="public"
else
    BRANCH="gh-pages"
    REPO="${1}"
    DIR=${2}
fi

(
    cd ${DIR} || exit
    git init
    git config user.name "Travis-CI"
    git config user.email "travis@ekara-platform.github.io"
    git add .
    git commit -m "Website generated by Hugo"
    git push --force --quiet "https://${GITHUB_TOKEN}@github.com/ekara-platform/${REPO}" master:${BRANCH} > /dev/null 2>&1
)
